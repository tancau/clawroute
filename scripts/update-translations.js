// Process each language file
const languages = ['es', 'fr', 'de', 'pt', 'ru', 'ar'];

languages.forEach(lang => {
  const filePath = path.join(messagesDir, `${lang}.json`);
  
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    const data = JSON.parse(content);
    
    // Update auth translations
    if (!data.auth) data.auth = {};
    Object.assign(data.auth, authTranslations[lang]);
    
    // Update dashboard translations
    if (!data.dashboard) data.dashboard = {};
    Object.assign(data.dashboard, dashboardTranslations[lang]);
    
    // Write back
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
    console.log(`Updated ${lang}.json`);
  } catch (error) {
    console.error(`Error updating ${lang}.json:`, error.message);
  }
});

console.log('\nAll translations updated!');
